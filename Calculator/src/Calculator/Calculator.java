    private void EnterNumbers(String q) {
        if (isResultDisplayed) {
            jtxtresult.setText(q);
            isResultDisplayed = false;
        } else {
            jtxtresult.setText(jtxtresult.getText() + q);
        }
    }
    private void setOperator(String operator) {
        if (!jtxtresult.getText().isEmpty()) {
            NumEnter1 = Double.parseDouble(jtxtresult.getText());
            op = operator;

            // format first number (remove .0)
            String num1Text = (NumEnter1 == (long) NumEnter1)
                    ? String.valueOf((long) NumEnter1)
                    : String.valueOf(NumEnter1);

            jtxtresult.setText(num1Text + operator); // ✅ show 5+
        }
    }
            
