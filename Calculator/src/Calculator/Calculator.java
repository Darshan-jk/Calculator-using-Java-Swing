    private void jbtnansActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnansActionPerformed
          String text = jtxtresult.getText();

            // extract second number after operator
            String secondPart = text.replaceAll(".*[+\\-*/%]", "");
            NumEnter2 = Double.parseDouble(secondPart);

            switch (op) {
                case "+":
                    Result = NumEnter1 + NumEnter2;
                    break;
                case "-":
                    Result = NumEnter1 - NumEnter2;
                    break;
                case "*":
                    Result = NumEnter1 * NumEnter2;
                    break;
                case "/":
                    Result = NumEnter1 / NumEnter2;
                    break;
                case "%":
                    Result = NumEnter1 % NumEnter2;
                    break;
            }

            // format result
            String resultText = (Result == (long) Result)
                    ? String.valueOf((long) Result)
                    : String.valueOf(Result);

            // format numbers
            String num1Text = (NumEnter1 == (long) NumEnter1)
                    ? String.valueOf((long) NumEnter1)
                    : String.valueOf(NumEnter1);

            String num2Text = (NumEnter2 == (long) NumEnter2)
                    ? String.valueOf((long) NumEnter2)
                    : String.valueOf(NumEnter2);

            jtxtresult.setText(num1Text + op + num2Text + "=" + resultText);

            isResultDisplayed = true;   
            
    }//GEN-LAST:event_jbtnansActionPerformed

